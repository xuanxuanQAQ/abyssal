/**
 * Recursive helper: widen all leaf string literals to `string`
 * so other locale files can assign different translated values.
 */
type DeepStringify<T> = {
  [K in keyof T]: T[K] extends string ? string : DeepStringify<T[K]>;
};

/** Shape constraint for translation files — keys must match en, values are any string. */
export type TranslationShape = DeepStringify<typeof en>;

/** Literal-typed schema used by react-i18next CustomTypeOptions for key autocompletion. */
export type TranslationSchema = typeof en;
